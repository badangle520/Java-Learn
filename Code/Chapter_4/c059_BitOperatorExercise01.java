public class c059_BitOperatorExercise01{

	//编写一个main方法
	public static void main(String[] args){
	
	
		//	~-5 = ?
		//	-5的原码: 10000000 00000000 00000000 00000101
		//	-5的反码: 11111111 11111111 11111111 11111010
		//	-5的补码: 11111111 11111111 11111111 11111011
		//	结果补码: 00000000 00000000 00000000 00000100
		//	结果原码: 00000000 00000000 00000000 00000100
		System.out.println(~-5);	//	4

		//	13&7 = ?
		//	13的补码: 00000000 00000000 00000000 00001101
		//	 7的补码: 00000000 00000000 00000000 00000111
		//	结果补码: 00000000 00000000 00000000 00000101
		//	结果原码: 00000000 00000000 00000000 00000101
		System.out.println(13&7);	//	5

		//	5|4 = ?
		//	 5的补码: 00000000 00000000 00000000 00000101
		//	 4的补码: 00000000 00000000 00000000 00000100
		//	结果补码: 00000000 00000000 00000000 00000101
		//	结果原码: 00000000 00000000 00000000 00000101
		System.out.println(5|4);	//	5
		
		//	-3^3 = ?
		//	-3的原码: 10000000 00000000 00000000 00000011
		//	-3的反码: 11111111 11111111 11111111 11111100
		//	-3的补码: 11111111 11111111 11111111 11111101
		//	 3的补码: 00000000 00000000 00000000 00000011
		//	结果补码: 11111111 11111111 11111111 11111110
		//	结果反码: 11111111 11111111 11111111 11111101
		//	结果原码: 10000000 00000000 00000000 00000010
		System.out.println(-3^3);	//	-2
	}
}
