public class c050_ConvertToDecimal{

	//编写一个main方法
	public static void main(String[] args){
	
		//1.1	二进制转十进制
		//	0b1011:	0b代表二进制
		//	1011 = 1*2的(1-1)次方 + 1*2的(2-1)次方 + 0*2的(3-1)次方 + 1*2的(4-1)次方 
		System.out.println(0b1011);	//	11
		
		//1.2	八进制转十进制
		//	0234: 0代表八进制
		//	234 = 4*8的(1-1)次方 + 3*8的(2-1)次方 + 2*8的(3-1)次方
		System.out.println(0234);	//	156 

		//1.3	十六进制转十进制
		//	0x23A: 0x代表16进制
		//	23A = 10*16的(1-1)次方 + 3*16的(2-1)次方 + 2*16的(3-1)次方
		System.out.println(0x23A);	//	570
	}

}
