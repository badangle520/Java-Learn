public class c061_BitOperatorExercise02{

	//编写一个main方法
	public static void main(String[] args){
	
		// -1的原码: 10000000 00000000 00000000 00000001
		// -1的反码: 11111111 11111111 11111111 11111110
		// -1的补码: 11111111 11111111 11111111 11111111
		//    -1>>2:   111111 11111111 11111111 11111111
		// 结果补码: 11111111 11111111 11111111 11111111
		// 结果反码: 11111111 11111111 11111111 11111110
		// 结果原码: 10000000 00000000 00000000 00000001
		int b = -1 >> 2;
		System.out.println(b);	//	-1

		// -1的原码: 10000000 00000000 00000000 00000001
		// -1的反码: 11111111 11111111 11111111 11111110
		// -1的补码: 11111111 11111111 11111111 11111111
		//    -1<<2: 11111111 11111111 11111111 111111
		// 结果补码: 11111111 11111111 11111111 11111100
		// 结果反码: 11111111 11111111 11111111 11111011
		// 结果原码: 10000000 00000000 00000000 00000100
		int d = -1 << 2;
		System.out.println(d);	//	-4

		//  3的补码: 00000000 00000000 00000000 00000011
		//    3>>>2:   000000 00000000 00000000 00000000 
		// 结果补码: 00000000 00000000 00000000 00000000
		// 结果原码: 00000000 00000000 00000000 00000000
		int e = 3 >>> 2;
		System.out.println(e);	//	0
	}
}
